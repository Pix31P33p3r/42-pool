#include <unistd.h>

int	ft_atoi(char *str)
{
	int	i;
	int	ich;
	int	res;

	i = 0;
	ich = 1;
	res = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			ich = -ich;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - 48;
		i++;
	}
	return (ich * res);
}

