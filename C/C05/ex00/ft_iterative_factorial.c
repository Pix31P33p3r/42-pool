#include <unistd.h>

int	ft_iterative_factorial(int nb)
{
	int	p;
	int	j;

	p = 1;
	j = 1;
	if (nb < 0)
		return (0);
	while (j <= nb)
	{
		p *= j;
		j++;
	}
	return (p);
}
